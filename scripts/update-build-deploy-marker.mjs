import { readFile, readdir, stat, writeFile } from 'node:fs/promises';
import crypto from 'node:crypto';
import path from 'node:path';

const markerPath = path.resolve('docs/BUILD-DEPLOY-MARKER.md');
const publicBuildDir = path.resolve('public/build');
const rootBuildDir = path.resolve('build');

async function pathExists(targetPath) {
  try {
    await stat(targetPath);
    return true;
  } catch {
    return false;
  }
}

async function collectFiles(dirPath, prefix = '') {
  const entries = await readdir(dirPath, { withFileTypes: true });
  const files = [];

  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    const relativePath = prefix ? `${prefix}/${entry.name}` : entry.name;
    const fullPath = path.join(dirPath, entry.name);

    if (entry.isDirectory()) {
      files.push(...await collectFiles(fullPath, relativePath));
      continue;
    }

    if (entry.isFile()) {
      files.push(relativePath);
    }
  }

  return files;
}

async function fingerprintDir(dirPath) {
  if (!(await pathExists(dirPath))) {
    return { exists: false, fingerprint: 'missing', fileCount: 0 };
  }

  const files = await collectFiles(dirPath);
  const hash = crypto.createHash('sha256');

  for (const relativePath of files) {
    const fullPath = path.join(dirPath, relativePath);
    const fileBuffer = await readFile(fullPath);
    hash.update(relativePath);
    hash.update('\n');
    hash.update(fileBuffer);
    hash.update('\n');
  }

  return {
    exists: true,
    fingerprint: hash.digest('hex'),
    fileCount: files.length,
  };
}

function buildMarkerContent(publicBuild, rootBuild) {
  const syncMode = process.env.SKIP_LEGACY_BUILD_SYNC === '1'
    ? 'skipped for this build'
    : 'enabled';
  const combinedHash = crypto.createHash('sha256')
    .update(publicBuild.fingerprint)
    .update('\n')
    .update(rootBuild.fingerprint)
    .digest('hex');

  return `# Build Deploy Marker

This file is auto-generated by \`npm run build\`.

If this file changes in git, the ignored build artifacts changed and should be manually uploaded before or with deployment:

- \`public/build/\`
- \`build/\` (legacy production compatibility)

Current mode:
- Legacy root build sync: ${syncMode}
- Local runtime still uses \`public/build/\`
- Current production compatibility still expects \`build/\`

Fingerprints:
- \`public/build/\`: ${publicBuild.fingerprint} (${publicBuild.fileCount} files)
- \`build/\`: ${rootBuild.fingerprint} (${rootBuild.fileCount} files)
- Combined fingerprint: ${combinedHash}

Do not edit this file manually.
`;
}

const publicBuild = await fingerprintDir(publicBuildDir);
const rootBuild = await fingerprintDir(rootBuildDir);
const nextContent = buildMarkerContent(publicBuild, rootBuild);

let currentContent = '';

if (await pathExists(markerPath)) {
  currentContent = await readFile(markerPath, 'utf8');
}

if (currentContent !== nextContent) {
  await writeFile(markerPath, nextContent);
  console.log('[build-marker] Updated docs/BUILD-DEPLOY-MARKER.md');
} else {
  console.log('[build-marker] No build artifact change detected.');
}
